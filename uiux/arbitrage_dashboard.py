# arbitrage_dashboard.py
# 목적:
# - 아비트라지(차익 거래) 전략의 성과를 모니터링하고, 관련 데이터를 시각적으로 제공.
# 목표:
# - 아비트라지 거래 실행 상태, 성공률, 수익 등을 실시간으로 확인.
# 구현해야 할 기능:
# 1. 전략 개요 섹션:
#    - 현재 실행 중인 아비트라지 페어(예: BTC/USDT vs BTC/KRW) 표시.
#    - 각 거래소 간의 가격 차이, 스프레드, 예상 수익률을 계산 및 표시.
# 2. 실시간 거래 내역 섹션:
#    - 최근 실행된 아비트라지 거래 내역 표시(거래 시간, 거래소, 수익, 상태 등).
# 3. 지표 시각화:
#    - 각 거래소의 가격 움직임 차트.
#    - 스프레드의 실시간 변동 그래프.
# 4. 성과 분석:
#    - 누적 수익, 성공 거래 비율, 평균 스프레드 등을 요약.
# 5. 사용자 입력 및 제어:
#    - 사용자가 특정 아비트라지 페어를 활성화/비활성화할 수 있는 인터페이스 제공.
#    - 거래소 API 상태를 모니터링하고 연결 상태를 시각적으로 표시.
# 6. 경고 시스템:
#    - 거래소 간 연결 오류, 스프레드 축소 등 중요한 경고를 사용자에게 알림.
# 7. 예외 처리:
#    - 아비트라지 전략 신호 생성 시 예외을 차지.
# 8. 대시보드 제공:
#    - 대시보드를 웹 페이지로 제공.