# arbitrage_features.py
# 목적: 아비트라지(차익거래)를 위한 피처 생성 및 지표 계산
# 목표:
# - 여러 거래소 간 가격 차이를 분석하여 차익거래 기회를 탐지
# - AI/ML 모델 학습에 필요한 아비트라지 관련 데이터를 생성
# - 실시간 분석 및 트레이딩에 활용 가능한 데이터 제공
# 구현해야 할 기능:
# 1. 거래소 간 가격 데이터 비교:
#    - Binance, Upbit 등 여러 거래소의 실시간 가격 데이터를 비교
#    - 스프레드(가격 차이)를 계산
# 2. 차익거래 기회 탐지:
#    - 특정 기준 이상의 가격 차이가 발생하는 거래소 쌍을 탐지
#    - 거래소 간 거래 수수료와 슬리피지를 고려하여 실질적인 기회만 필터링
# 3. 피처 생성:
#    - 거래소별 유동성, 거래량 등 아비트라지와 관련된 주요 피처 생성
#    - 시간대별 스프레드 패턴 및 변동성 분석 데이터 추가
# 4. 데이터 정규화 및 스케일링:
#    - AI/ML 모델 학습용 데이터를 정규화하여 처리
#    - 거래소별 데이터 불균형을 최소화
# 5. 실시간 및 백테스트 지원:
#    - 실시간 아비트라지 기회 데이터를 반환
#    - 과거 데이터를 기반으로 한 아비트라지 백테스트 결과 생성
# 6. 시각화:
#    - 아비트라지 기회를 차트 형태로 시각화 (예: 스프레드 트렌드 그래프)
