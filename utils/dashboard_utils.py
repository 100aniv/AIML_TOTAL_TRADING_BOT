# dashboard_utils.py
# 목적:
# - 대시보드와 관련된 공통 유틸리티 기능 제공.
# 목표:
# - 대시보드 개발에서 중복되는 기능을 모듈화하여 코드 간결성 유지.
# 구현해야 할 기능:
# 1. 차트 데이터 준비:
#    - 캔들 차트, 지표 그래프 등의 데이터 포맷팅.
# 2. 사용자 입력 처리:
#    - 대시보드에서 받은 설정값을 백엔드로 전달.
# 3. 실시간 데이터 변환:
#    - 실시간 데이터를 대시보드에서 렌더링 가능한 형식으로 변환.
# 4. 대시보드 성능 최적화:
#    - 실시간 데이터 업데이트를 효율적으로 처리.
# 5. 대시보드 홈페이지 제작:
#    - 대시보드 홈페이지를 제작하여 사용자에게 품목보이기.