# generator.py
# 목적: 매수/매도 신호 생성
# 목표:
# - AI/ML 모델의 출력과 지표 데이터를 기반으로 매수/매도 신호를 생성
# - 다양한 전략에 따른 신호 생성 규칙 구현
# 구현해야 할 기능:
# 1. 신호 생성 규칙:
#    - AI/ML 모델 출력에 기반한 신호 생성
#    - 지표 데이터(예: RSI, MACD, MA 교차 등)를 기반으로 한 조건부 신호 생성
#    - 복합 신호 생성: 여러 조건을 결합한 신호 로직 구현
# 2. 신호 유형 관리:
#    - 매수 신호, 매도 신호, 홀딩 신호 구분
#    - 신호의 강도(신뢰도) 값 추가
# 3. 사용자 정의 신호:
#    - 사용자가 원하는 조건에 따라 신호 생성
# 4. 예외 처리:
#    - 데이터 결측 또는 계산 오류 발생 시 기본값 반환
# 5. 시각화:
#    - 신호와 신호 강도를 차트 형태로 시각화
# 6. 로그로 기록:
#    - 신호 생성 시점, 강도, 데이터 조건 등을 로그로 기록
# 7. 모델 업데이트:
#    - 신호 생성 시점, 강도, 데이터 조건 등을 모델 업데이트 시각화를 위한 데이터 저장